/**
 * abstraction means hiding implementations.
 * there are two ways to achieve  abstraction:
 * 1.using abstract class (0-100%)
 * 2.using interface (100%) abstraction.
 * normal class:
 * class AA{
 *
 *
 * }
 * abstract class :
 * abstract class AA{
 *
 *
 * }
 * abstract class can have abstract or non abstract method.
 * we cant create object of an abstract class
 * normal method :
 * void display(){
 *     ------
 *     ---
 * }
 * abstract method:
 * abstract void display();
 *
 * points to remember :
 * 1.abstract method has no body.
 * 2.it must be ends with a semicolon(;).
 * 3.it must be in abstract class.
 * 4.it must be overridden.
 * 5.it will never be used as static or final.
 */
